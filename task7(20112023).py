import random

def charivna_kulka(question):
    """
    Моделює роботу "чарівної кульки передбачень".

    Параметри:
    - question (str): Питання, на яке потрібно отримати відповідь.

    Повертає:
    str: Випадково обрана відповідь зі списку можливих варіантів.

    Приклад використання:
    >>> charivna_kulka("Чи отримаю я сьогодні промоцію?")
    "Так"
    """
    possible_answers = ["Так", "Ні", "Можливо", "Не зовсім впевнений", "Спробуйте ще раз пізніше"]
    return random.choice(possible_answers)

# Тести
def test_charivna_kulka():
    # Перевірка, що функція повертає одне з очікуваних значень
    result = charivna_kulka("Чи сьогодні буде дощ?")
    assert result in ["Так", "Ні", "Можливо", "Не зовсім впевнений", "Спробуйте ще раз пізніше"], "Некоректна відповідь"

    # Перевірка, що функція повертає значення типу str
    assert isinstance(result, str), "Тип відповіді не є str"

    # Перевірка реакції на пусте питання
    result_empty_question = charivna_kulka("")
    assert result_empty_question in ["Так", "Ні", "Можливо", "Не зовсім впевнений", "Спробуйте ще раз пізніше"], "Некоректна відповідь на пусте питання"

    # Перевірка реакції на введення, яке не є рядком
    try:
        charivna_kulka(123)
    except TypeError as e:
        assert str(e) == "Параметр 'question' має бути рядком", "Некоректна реакція на введення, яке не є рядком"

    print("Тести пройдено успішно.")

# Запуск тестів
test_charivna_kulka()